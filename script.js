function processData() {
    let input = document.getElementById("inputData").value;
    let lines = input.split('\n');
    let output = [];

    for (let line of lines) {
        let stockNumber = line.split('\t')[0].slice(0, 4);
        let marketType = stockDatabase[stockNumber];
        if (marketType) {
            output.push(`${marketType}:${stockNumber}`);
        } else {
            console.log(`未找到股票代號: ${stockNumber}`);
        }
    }

    document.getElementById("output").textContent = output.join(',\n');
}

function downloadList() {
    let content = document.getElementById("output").textContent;
    let date = new Date();
    let formattedDate = date.toISOString().slice(0, 10).replace(/-/g, '');
    let fileName = `台股RS_${formattedDate}.txt`;

    let blob = new Blob([content], { type: "text/plain" });
    let link = document.createElement("a");
    link.href = URL.createObjectURL(blob);
    link.download = fileName;
    link.click();
}
